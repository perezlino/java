/*  ======================================================
    ============= FLUJO DE CONTROL - BUCLES ==============
    ======================================================

    
    WHILE
    =====

    - Ejecuta un bloque de código mientras se cumpla la condición

    - Es una estructura de iteración precondición, primero evalúa la expresión antes de ejecutar
      cualquier sentencia.
     _________________________________________________________________________
    |                                                                         |              
    |    while(expresion){                                                    |      
    |        // ejecuta el bloque de código mientras se cumpla la condición   |
    |    }                                                                    |          
    |_________________________________________________________________________|


    DO WHILE
    ========

    - Similar a While, excepto en que la expresión se evalúa al final del bucle, se ejcuta al menos
      una vez.

    - Es una estructura de iteración postcondición, primero ejecuta la sentencia y al final evalúa la 
      expresión
     _________________________________________________________________________
    |                                                                         |              
    |    do{                                                                  |      
    |        // ejecuta el bloque de código al menos una vez                  |
    |    } while(expresion);                                                  |          
    |_________________________________________________________________________|


    FOR
    ===

    - La sentencia FOR se utiliza cuando se conocen los límites del bucle (inicialización, su condición
      de término y su instrucción de incremento/decremento)
     _________________________________________________________________________
    |                                                                         |              
    |    for(inicialización; condición; incremento){                          |      
    |        // el bloque de iteración se ejecuta si cumple la condición      |
    |    }                                                                    |          
    |_________________________________________________________________________|
*/